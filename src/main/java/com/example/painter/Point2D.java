package com.example.painter;

public class Point2D {
    private final double x;
    private final double y;

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }

    public Point2D(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
